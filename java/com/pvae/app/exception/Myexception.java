package com.pvae.app.exception;

public class Myexception extends Exception {

      public Myexception() {
            super();
      }

      public Myexception(String message) {
            super(message);
      }

      public Myexception(Throwable cause) {
            super(cause);
      }

    
      public Myexception(String message, Throwable cause) {

            super(message, cause);
    
        }



}
